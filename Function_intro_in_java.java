public class Function_intro_in_java
{
	// Function
	public static void printMessage()
	{
		System.out.println("This is a simple function!");
	}
	public static void main(String[] args) {
		printMessage();
	}
}
/*
Introduction of function in java:

	In java you can't define function out side of class.

	In java function can overload like in c++.

	In java you must have declare function static inside the main class. 

	Java always uses pass-by-value for method arguments. 
*/